public class VariableDemo1{
	//主入口
	public static void main(String[] args){
		//定义变量
		//数据类型：限定了变量能存储数据的类型
		//int（整数） double （小数）
		//变量名：就是存储空间的名字
		//作用：方便以后使用
		//数据值：真正存在变量中的数据
		//定义一个整数类型的变量
		//数据类型 变量名 = 数据值;
		int a = 16;
		System.out.println(a);//16
		
		//定义一个小数类型的变量
		double b = 10.1;
		System.out.println(b);//10.1
	}
}